#include  "ft_printf.h"

int ft_putptr(unsigned long long ptr)
{
	int i;

	i = 0;
	while (ptr >= 16)
	{
		i++;
		ptr /= 16;
	}
	ft_putchar("0123456789abcdef"[ptr]);
	return i + 1;
}
